import random

HAFEZ_POEMS = [
    # چند مثالی، تو می‌تونی کل دیوان حافظ را اضافه کنی
    "الا یا ایها الساقی  که عشق می‌طلبد ساقی …",
    "دوش ‌آمد به سرم صفرایی ز می‌ساقی …",
    "زلف بر باد مده تا ندهی بر بادم دست …",
    "گفتم غم تو دارم گفتا غمت سر آید …"
    # ... (اینجا کل دیوان حافظ را قرار دهید)
]

def get_random_hafez() -> str:
    return random.choice(HAFEZ_POEMS)
